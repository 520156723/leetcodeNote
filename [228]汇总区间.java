class Solution {
    public List<String> summaryRanges(int[] nums) {
        int i = 0;
        ArrayList<String> res = new ArrayList<>();
        while (i < nums.length) {
            int lo = i;
            i++;
            while (i < nums.length && nums[i] == nums[i - 1] + 1)
                i++;
            int hi = i - 1;
            StringBuilder tmp = new StringBuilder();
            tmp.append(nums[lo]);
            if (hi > lo) {
                tmp.append("->").append(nums[hi]);
            }
            res.add(tmp.toString());
        }
        return res;
    }
}
