import java.util.HashMap;

/**
 * Definition for a binary tree node.
 * public class TreeNode {
 *     int val;
 *     TreeNode left;
 *     TreeNode right;
 *     TreeNode() {}
 *     TreeNode(int val) { this.val = val; }
 *     TreeNode(int val, TreeNode left, TreeNode right) {
 *         this.val = val;
 *         this.left = left;
 *         this.right = right;
 *     }
 * }
 */
class Solution {
    HashMap<Integer, Integer> map = new HashMap<>();//ä¸­åºéå†ä¸­çš„å€¼-ä¸‹æ ‡
    public TreeNode buildTree(int[] preorder, int[] inorder) {
        for (int i = 0; i < inorder.length; i++) {
            map.put(inorder[i], i);
        }
        return build(preorder, 0, preorder.length - 1, inorder, 0, inorder.length - 1);
    }

    TreeNode build(int[] preorder, int pl, int pr, int[] inorder, int il, int ir) {
        if(pl > pr || il > ir)
            return null;
        int rootIndex = map.get(preorder[pl]);
        int leftlen = rootIndex - il;
        TreeNode root = new TreeNode(preorder[pl]);
        root.left = build(preorder,pl + 1,pl + leftlen,inorder, il, rootIndex - 1);
        root.right = build(preorder,pl + leftlen + 1,pr, inorder, rootIndex + 1, ir);
        return root;
    }
}
