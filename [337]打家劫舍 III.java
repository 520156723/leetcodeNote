class Solution {
    //int[0], int[1]åˆ†åˆ«è¡¨ç¤ºå·è¯¥èŠ‚ç‚¹å’Œä¸å·è¯¥èŠ‚ç‚¹çš„æ€»é‡‘é¢ï¼Œååºéå†å¾—åˆ°æ¯ä¸ªèŠ‚ç‚¹å·æˆ–ä¸å·çš„æ€»é‡‘é¢
    //æœ€åå¾—åˆ°æ ¹èŠ‚ç‚¹çš„æœ€å¤§æ€»é‡‘é¢
    public int rob(TreeNode root) {
        int[] res = dfs(root);
        return Math.max(res[0], res[1]);
    }

    int[] dfs(TreeNode root) {
        if(root == null)
            return new int[]{0, 0};
        int[] l = dfs(root.left);
        int[] r = dfs(root.right);
        int select = root.val + l[1] + r[1];
        //ä¸å·æ ¹èŠ‚ç‚¹ï¼Œå–å·¦å­æ ‘æœ€å€¼+å³å­æ ‘
        int notSelect = Math.max(l[0], l[1]) + Math.max(r[0], r[1]);
        return new int[]{select, notSelect};
    }
}
