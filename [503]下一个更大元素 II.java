    public int[] nextGreaterElements(int[] nums) {
        int n = nums.length;
        int[] ans = new int[n];
        Arrays.fill(ans, -1);//æå‰ä¸ºæœ€åå‰©ä¸‹çš„èµ‹å€¼
        Deque<Integer> d = new ArrayDeque<>();
        for (int i = 0; i < n * 2 - 1; i++) {
            while (!d.isEmpty() && nums[i % n] > nums[d.peekLast()])
                ans[d.pollLast()] = nums[i % n];
            d.addLast(i % n);
        }
        return ans;
    }
