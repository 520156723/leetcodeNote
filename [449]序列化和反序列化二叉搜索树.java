import java.util.LinkedList;

/**
 * Definition for a binary tree node.
 * public class TreeNode {
 *     int val;
 *     TreeNode left;
 *     TreeNode right;
 *     TreeNode(int x) { val = x; }
 * }
 */
public class Codec {
    // Encodes a tree to a single string.
    public String serialize(TreeNode root) {
        if(root == null)
            return "#";
        return serializeImp(root, new StringBuilder()).toString();
    }

    public StringBuilder serializeImp(TreeNode root, StringBuilder sb) {
        if(root == null){
            sb.append("#");
            return sb;
        }
        sb.append(root.val).append(" ");
        sb = serializeImp(root.left, sb).append(" ");
        return serializeImp(root.right, sb);
    }

    // Decodes your encoded data to tree.
    public TreeNode deserialize(String data) {
        if(data.isEmpty())
            return null;
        LinkedList<String> queue = new LinkedList<>();
        for (String s : data.split(" "))
            queue.addLast(s);
        return helper(queue);
    }

    TreeNode helper(LinkedList<String> queue){
        String s = queue.removeFirst();
        if(s.equals("#"))
            return null;
        TreeNode root = new TreeNode(Integer.valueOf(s));
        root.left = helper(queue);
        root.right = helper(queue);
        return root;
    }
}

