    // ä¸ºä»€ä¹ˆç­‰äºçš„ä¹Ÿè¦æ¢ï¼Œå› ä¸ºtailä¸­è¦ä¸¥æ ¼é€’å¢ï¼Œå³æ²¡ç”¨ç›¸åŒå…ƒç´ 
    public int lengthOfLIS(int[] nums) {
        int[] tail = new int[nums.length];
        tail[0] = nums[0];//ç¬¬ä¸€ä¸ªå…ƒç´ ä½œä¸ºåˆå§‹å­åºåˆ—
        int end = 0;//tailå°¾ä¸‹æ ‡
        for (int i = 0; i < nums.length; i++) {
            if (nums[i] > tail[end]) {
                tail[++end] = nums[i];
            }else {
                int l = 0, r = end;
                while (l < r) {
                    int mid = l + (r - l >> 1);
                    if (nums[i] > tail[mid]) {
                        l = mid + 1;
                    }else {
                        r = mid;
                    }
                }
                tail[l] = nums[i];
            }
        }
        return end + 1;
    }
